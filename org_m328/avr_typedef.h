typedef unsigned int  word;     			// 16bit data size
typedef	unsigned char uint8_t;				// 8bit unsigned char
typedef unsigned int  uint16_t;				// 16bit unsigned int
typedef unsigned long uint32_t;				// 32bit unsigned int
typedef char	      int8_t;				// 8bit signed char
typedef int	      int16_t;				// 16bit signed int
typedef long int      int32_t;				// 32bit signed int



